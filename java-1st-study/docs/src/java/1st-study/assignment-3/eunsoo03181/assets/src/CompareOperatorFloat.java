package WEEK03;

public class CompareOperatorFloat {
    public static void main(String[] args) {
        float pi1 = 3.14f;
        double pi2 = 3.14;
        long va1 = 2L;
        int va2 = 2;
        System.out.println("pi1과 pi2는 같은 값입니까? " + (pi1 == pi2));
        System.out.println("va1과 va2는 같은 값입니까? " + (va1 == va2));
    }
}
