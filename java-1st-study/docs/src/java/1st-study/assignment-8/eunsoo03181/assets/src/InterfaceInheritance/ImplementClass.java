package WEEK08.InterfaceInheritance;

public class ImplementClass implements Child{
    @Override
    public void method1() {
        System.out.println("1번 메소드");
    }

    @Override
    public void method2() {
        System.out.println("2번 메소드");
    }

    @Override
    public void method3() {
        System.out.println("3번 메소드");
    }

    @Override
    public void method4() {
        System.out.println("4번 메소드");
    }
}
